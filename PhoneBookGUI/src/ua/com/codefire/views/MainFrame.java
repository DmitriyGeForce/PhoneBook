    private void jTableContactsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableContactsMouseClicked
        // TODO add your handling code here:
        int id = jTableContacts.getSelectedRow();
        DefaultTableModel tableModel = (DefaultTableModel) jTableContacts.getModel();
        if (id != -1) {
            ImageIcon icon = controller.getImage((int) tableModel.getValueAt(id, 0));
            ImageIcon imageIcon = new ImageIcon(icon.getImage().getScaledInstance(jLabelPhoto.getWidth(), jLabelPhoto.getHeight(), Image.SCALE_SMOOTH));
            jLabelPhoto.setIcon(imageIcon);
        }
        jTableContacts.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
        public void valueChanged(ListSelectionEvent event) {
            // do some actions here, for example
            // print first column value from selected row
        }
    });
    }//GEN-LAST:event_jTableContactsMouseClicked

    private void jButtonChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTableContacts.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel tableModel = (DefaultTableModel) jTableContacts.getModel();
           int id = (int) tableModel.getValueAt(selectedRow, 0);
           String name = String.valueOf(tableModel.getValueAt(selectedRow, 1));
           String phone = String.valueOf(tableModel.getValueAt(selectedRow, 2));
           AddFrame frame = new AddFrame(new Contact(id, name, phone));
           frame.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Choose contact you want to change");
        }
    }//GEN-LAST:event_jButtonChangeActionPerformed

