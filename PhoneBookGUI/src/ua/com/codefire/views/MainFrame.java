    private void jButtonFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFindActionPerformed
        String name = JOptionPane.showInputDialog(rootPane, "Please, type name");
        List<Contact> list = controller.getContactByName(name);
        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Can't find any contact with name " + name);
        } else {
            DefaultTableModel tableModel = (DefaultTableModel) jTableContacts.getModel();
            tableModel.getDataVector().clear();
            for (Contact contact : list) {
                tableModel.addRow(new Object[]{
                    contact.getId(), contact.getName(), contact.getPhone()
                });
            }
        }
    }//GEN-LAST:event_jButtonFindActionPerformed

